<br /><br />
Ms. Barua launched the distinguished "Forum for Women in Leadership" in 2007 - that brings together senior women executives from leading companies in India and multinationals on the mission and mandate for leveraging the vast talent-pool of women, sharing best practices for women in the workplace, and mentoring women for leadership positions. Over 6,000 senior women and men professionals, from across 250 companies, and over 750 WILL Mentees - are participating in the WILL Forum programs and mission. Founding members of the WILL Forum India include Tata Consultancy Services, KPMG, Infosys Technologies, and Aditya Birla Group.
<br /><br />
Ms. Barua is Author of the pioneering Book "Leadership by Proxy: The Story of Women in Corporate India" (Bloomsbury) and has written several Research Reports on "WILL Handbook:50- Best Practices for Women in the Workplace," "Differentiating Styles of Women in Leadership" and the best selling report "Balanced Boards for Good Governance." She is presently writing the landmark Report on "Creating Digital Women - for the Digital Era!" - with distinguished sponsorship from Capgemini.
<br /><br />

For over a decade, Ms. Barua has been Regional Director - India, The Conference Board, New York, providing thought-leadership on best business practices for companies in India. She is actively committed to bringing transparency and corporate governance, and serves as an Independent Director on corporate boards, leads the pioneering "WILL Women on Corporate Boards: Series" in India, and is a Member of the Joint Electricity Regulation Commission Advisory Board, of the Indian government.
<br /><br />
Ms. Barua has been awarded a Visiting Scholarship at The Wharton School Advanced Management Program for her work on women in leadership. She also holds a Ford Fellowship at the Paul H. Nitze School of Advanced International Studies (SAIS), Johns Hopkins University, for her work on conflict resolution in South Asia. She is a Visiting Fellow at the Henry L. Stimson Center (Washington D.C.) and the Salzburg Seminar in Austria. Ms. Barua has also served as Special Consultant for distinguished international institutions - including the Eisenhower Fellowships, London Business School, and East-West Center.
<br /><br />
Ms. Barua is a trained Economist with a Master's Degree from the prestigious Delhi School of Economics, and has served as Chief Program Specialist for over a decade at the United States Information Service in New Delhi. She has also been a Lecturer in Economics at Delhi University and Research Associate at the Indian Institute of Public Administration.
